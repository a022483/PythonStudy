x=True;
y=False;
print('NOT：',not x)         #反閘
print('OR：',x|y)            #OR
print('AND：',x&y)           #AND
print('NOR',not(x|y))        #NOR
print('NAND',not(x&y))       #NAND
print('XOR',x^y)             #NOR
print('NXOR',not(x^y))       #XNOR
print('0b011'+'<<'+'0b110',3<<1)     #以二進制左移一個位元
print('0b010'+'>>'+'0b001',2>>1)     #以二進制右移一個位元
